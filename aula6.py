# Conversão de tipos, coerção
# type convertion, typecasting, coeercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool
print(1 + 1)# Soma
print('a' + 'b')# Concatena
print('1', type('1'))
print(int('1'), type(int('1'))) #Conversão str para int
print(bool('')) #Retorna False
print(bool(' ')) #Retorna True
print(str(11) + 'b') #Conversão int para str