# Variáveis são usadas para salvar algo na memória do computador.
# PEP 8: inicie variáveis com letras minúsculas, pode usar números e underline _.
# O sinal de = é o operador de atribuição. Ele é usado para atribuir um valor a um nome(variável).
# Uso: nome_variável = expressão

nome_completo = 'Lucas Martins'
print(nome_completo) # lucas Martins

soma = 2 + 2
print(soma) # 4

idade = 23
maior_idade = idade >= 18
print(maior_idade)